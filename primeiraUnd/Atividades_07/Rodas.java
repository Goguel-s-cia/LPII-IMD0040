package primeiraUnd.Atividades_07;

public class Rodas {
	private int tamanho;
	private String material;
	
	public Rodas(int tamanho, String material) {
		this.tamanho = tamanho;
		this.material = material;
	}
	
	public int getTamanho() {
		return tamanho;
	}
	public String getMaterial() {
		return material;
	}

}
