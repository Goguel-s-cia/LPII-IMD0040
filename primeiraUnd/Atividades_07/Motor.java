package primeiraUnd.Atividades_07;

public class Motor {
	private String modelo;
	private int potencia;
	
	public Motor(String modelo, int potencia) {
		this.modelo = modelo;
		this.potencia = potencia;
	}
	
	public String getModelo() {
		return modelo;
	}
	public int getPotencia() {
		return potencia;
	}
}
